// Function to get random HD image for a product
export const getRandomHDImage = (productName) => {
  const product = hdProductImages[productName];
  if (product && product.urls && product.urls.length > 0) {
    const randomIndex = Math.floor(Math.random() * product.urls.length);
    return product.urls[randomIndex];
  }
  return null;
};

// Function to get all images for a product
export const getAllImagesForProduct = (productName) => {
  const product = hdProductImages[productName];
  return product ? product.urls : [];
};

// Function to download and save image locally
export const downloadImageLocally = async (imageUrl, fileName) => {
  try {
    const response = await fetch(imageUrl);
    const blob = await response.blob();
    
    // Create download link
    const url = window.URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = url;
    link.download = fileName;
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
    window.URL.revokeObjectURL(url);
    
    return true;
  } catch (error) {
    console.error('Error downloading image:', error);
    return false;
  }
};
